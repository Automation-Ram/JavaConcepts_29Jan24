package com.qa.javabasics;

public class Switch {

	public static void main(String[] args) {

		int number = 2;

		switch (number) {
		case 1: {
			System.out.println("Value 1");
			break;

		}
		case 2: {
			System.out.println("Value 2");
			

		}
		case 3: {
			System.out.println("Value 3");
			

		}
		default:
			System.out.println("At any cost");
		}

	}

}
